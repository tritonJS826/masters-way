package routers

import (
	"context"
	"mw-server/internal/auth"
	"mw-server/internal/config"
	"mw-server/internal/openapi"
	"net/http"
	"testing"

	openapiGeneral "mw-server/apiAutogenerated/general"

	"github.com/samber/lo"
	"github.com/stretchr/testify/assert"
)

func TestCreateUserProject(t *testing.T) {
	newConfig, err := config.LoadConfig("../../")
	if err != nil {
		t.Fatalf("Failed to load config: %v", err)
	}

	generalApi := openapi.MakeGeneralAPIClient(&newConfig)
	_, err = generalApi.DevAPI.DevResetDbGet(context.Background()).Execute()
	if err != nil {
		t.Fatalf("Failed to reset db: %v", err)
	}

	projectID := "afb02990-7e8c-4353-a724-ea8de5fb6cfc"
	projectOwnerID := "1b3d5e7f-5a1e-4d3a-b1a5-d1a1d5b7a7e1"
	firstNewProjectUserID := "5a31e3cb-7e9a-41e5-9a3b-1f1e5d6b7c3e"

	t.Run("should create a UserProject successfully and check if the user is added to the project", func(t *testing.T) {
		token, err := auth.GenerateJWT(projectOwnerID, newConfig.SecretSessionKey, auth.AccessExpIn)
		if err != nil {
			t.Fatalf("Failed to generate JWT: %v", err)
		}

		request := openapiGeneral.MwServerInternalSchemasCreateUserProjectPayload{
			ProjectId: projectID,
			UserId:    firstNewProjectUserID,
		}

		ctx := context.WithValue(context.Background(), auth.ContextKeyAuthorization, "Bearer "+token)
		userProjectResponse, err := generalApi.UserProjectAPI.CreateUserProject(ctx).Request(request).Execute()
		if err != nil {
			t.Fatalf("Failed to create userProject: %v", err)
		}

		assert.Equal(t, http.StatusNoContent, userProjectResponse.StatusCode)

		project, projectResponse, err := generalApi.ProjectAPI.GetProject(ctx, projectID).Execute()
		if err != nil {
			t.Fatalf("Failed to create userProject: %v", err)
		}

		assert.Equal(t, http.StatusOK, projectResponse.StatusCode)

		_, found := lo.Find(project.Users, func(user openapiGeneral.MwServerInternalSchemasUserPlainResponseWithInfo) bool {
			return user.Uuid == firstNewProjectUserID
		})

		assert.True(t, found, "User with the given ID was not found in the project")
	})
}

func TestDeleteUserProject(t *testing.T) {
	newConfig, err := config.LoadConfig("../../")
	if err != nil {
		t.Fatalf("Failed to load config: %v", err)
	}

	generalApi := openapi.MakeGeneralAPIClient(&newConfig)
	_, err = generalApi.DevAPI.DevResetDbGet(context.Background()).Execute()
	if err != nil {
		t.Fatalf("Failed to reset db: %v", err)
	}

	projectID := "afb02990-7e8c-4353-a724-ea8de5fb6cfc"
	projectOwnerID := "1b3d5e7f-5a1e-4d3a-b1a5-d1a1d5b7a7e1"

	t.Run("should delete a UserProject successfully and check if the user is deleted from the project", func(t *testing.T) {
		userID := "7cdb041b-4574-4f7b-a500-c53e74c72e94"

		token, err := auth.GenerateJWT(projectOwnerID, newConfig.SecretSessionKey, auth.AccessExpIn)
		if err != nil {
			t.Fatalf("Failed to generate JWT: %v", err)
		}

		ctx := context.WithValue(context.Background(), auth.ContextKeyAuthorization, "Bearer "+token)
		userProjectResponse, err := generalApi.UserProjectAPI.DeleteUserProject(ctx, projectID, userID).Execute()
		if err != nil {
			t.Fatalf("Failed to create userProject: %v", err)
		}

		assert.Equal(t, http.StatusNoContent, userProjectResponse.StatusCode)

		project, projectResponse, err := generalApi.ProjectAPI.GetProject(ctx, projectID).Execute()
		if err != nil {
			t.Fatalf("Failed to create userProject: %v", err)
		}

		assert.Equal(t, http.StatusOK, projectResponse.StatusCode)

		_, found := lo.Find(project.Users, func(user openapiGeneral.MwServerInternalSchemasUserPlainResponseWithInfo) bool {
			return user.Uuid == userID
		})

		assert.True(t, !found, "The owner of the project was unable to remove a user from the project.")
	})
}
