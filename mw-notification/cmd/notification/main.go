package main

import (
	"context"
	"fmt"
	"log"
	"mw-notification/internal/config"
	"mw-notification/internal/controllers"
	"mw-notification/internal/services"
	"mw-notification/pkg/database"
	pb "mw-notification/pkg/grpcAutoGenerated/notification"
	"net"
	"os"
	"os/signal"
	"syscall"
	"time"

	"google.golang.org/grpc"
	"google.golang.org/grpc/reflection"
)

func main() {
	newConfig, err := config.LoadConfig("./")
	if err != nil {
		fmt.Fprintf(os.Stderr, "cannot load config: %v\n", err)
		os.Exit(1)
	}

	newPool, err := database.NewPostgresDB(&newConfig)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Unable to create connection pool: %v\n", err)
		os.Exit(1)
	}
	defer newPool.Close()

	newService := services.NewService(newPool)
	newController := controllers.NewController(newService)

	lis, err := net.Listen("tcp", fmt.Sprintf(":%s", newConfig.ServerPort))
	if err != nil {
		log.Fatalf("failed to listen: %v", err)
	}

	grpcServer := grpc.NewServer()
	reflection.Register(grpcServer)

	pb.RegisterNotificationServer(grpcServer, newController.NotificationController)
	pb.RegisterEnabledNotificationServer(grpcServer, newController.EnabledNotificationController)
	pb.RegisterDevServer(grpcServer, newController.DevController)

	go func() {
		log.Printf("gRPC server listening at %v", lis.Addr())
		if err := grpcServer.Serve(lis); err != nil {
			log.Fatalf("failed to serve: %v", err)
		}
	}()

	quit := make(chan os.Signal, 1)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)
	<-quit
	log.Println("Shutdown initiated...")

	ctx, cancel := context.WithTimeout(context.Background(), 3*time.Second)
	defer cancel()

	done := make(chan struct{})
	go func() {
		grpcServer.GracefulStop()
		close(done)
	}()

	select {
	case <-done:
		log.Println("gRPC server shutdown completed")
	case <-ctx.Done():
		log.Println("gRPC server shutdown timed out")
	}

	log.Println("Server exiting")
}
