package main

import (
	"context"
	"fmt"
	database "go-common/database"
	"log"
	"mw-training/internal/config"
	"mw-training/internal/controllers"
	"mw-training/internal/services"
	"net"
	"os"
	"os/signal"
	"syscall"
	"time"

	pb "mw-training/pkg/grpcAutogenerated/training"

	"google.golang.org/grpc"
	"google.golang.org/grpc/reflection"
)

func main() {
	newConfig, err := config.LoadConfig("./")
	if err != nil {
		fmt.Fprintf(os.Stderr, "cannot load config: %v\n", err)
		os.Exit(1)
	}

	// This line executed with no errors only with empty db
	// So it is important for first microservice deploy
	database.RunMigrations(newConfig.DBSource)

	newPool, err := database.NewPostgresDB(newConfig.DBSource)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Unable to create connection pool: %v\n", err)
		os.Exit(1)
	}
	defer newPool.Close()

	newService := services.NewService(newPool)
	newController := controllers.NewController(newService)

	lis, err := net.Listen("tcp", fmt.Sprintf(":%s", newConfig.ServerPort))
	if err != nil {
		log.Fatalf("failed to listen: %v", err)
	}

	grpcServer := grpc.NewServer()
	reflection.Register(grpcServer)

	pb.RegisterTrainingTagServiceServer(grpcServer, newController.TrainingTagsController)
	pb.RegisterTrainingServiceServer(grpcServer, newController.TrainingsController)
	pb.RegisterTrainingMentorServiceServer(grpcServer, newController.TrainingMentorsController)
	pb.RegisterTrainingStudentServiceServer(grpcServer, newController.TrainingStudentController)
	pb.RegisterFavoriteUsersTrainingsServiceServer(grpcServer, newController.FavoriteUsersTrainingsController)
	pb.RegisterPracticeMaterialServiceServer(grpcServer, newController.PracticeMaterialsController)
	pb.RegisterTheoryMaterialServiceServer(grpcServer, newController.TheoryMaterialsController)
	pb.RegisterTopicsServiceServer(grpcServer, newController.TopicsController)
	pb.RegisterDevServer(grpcServer, newController.DevController)

	go func() {
		log.Printf("gRPC server listening at %v", lis.Addr())
		if err := grpcServer.Serve(lis); err != nil {
			log.Fatalf("failed to serve: %v", err)
		}
	}()

	quit := make(chan os.Signal, 1)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)
	<-quit
	log.Println("Shutdown initiated...")

	ctx, cancel := context.WithTimeout(context.Background(), 3*time.Second)
	defer cancel()

	done := make(chan struct{})
	go func() {
		grpcServer.GracefulStop()
		close(done)
	}()

	select {
	case <-done:
		log.Println("grpcServer shutdown completed")
	case <-ctx.Done():
		log.Println("Server shutdown completed or timed out")
	}

	log.Println("Server exiting")
	// TODO: Ask kirill why do we need this line in REST services and don't have such line in grpc
	os.Exit(0)
}
