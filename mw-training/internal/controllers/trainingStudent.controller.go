package controllers

import (
	"context"

	db "mw-training/internal/db/sqlc"
	services "mw-training/internal/services"
	pb "mw-training/pkg/grpcAutogenerated/training"

	"github.com/google/uuid"
	"github.com/jackc/pgx/v5/pgtype"
	"google.golang.org/protobuf/types/known/emptypb"
)

type TrainingStudentController struct {
	trainingStudentService *services.TrainingStudentService
	pb.TrainingStudentServiceServer
}

func NewTrainingStudentController(trainingStudentService *services.TrainingStudentService) *TrainingStudentController {
	return &TrainingStudentController{trainingStudentService: trainingStudentService}
}

func (ftc *TrainingStudentController) CreateTrainingStudent(ctx context.Context, in *pb.CreateTrainingStudentRequest) (*emptypb.Empty, error) {
	studentUuid := in.GetStudentUuid()
	trainingUuid := in.GetTrainingUuid()
	arg := db.CreateTrainingStudentParams{
		TrainingUuid: pgtype.UUID{Bytes: uuid.MustParse(trainingUuid), Valid: true},
		StudentUuid:  pgtype.UUID{Bytes: uuid.MustParse(studentUuid), Valid: true},
	}

	_, err := ftc.trainingStudentService.CreateTrainingStudent(ctx, arg)
	if err != nil {
		return nil, err
	}

	return &emptypb.Empty{}, nil
}

func (ftc *TrainingStudentController) DeleteTrainingStudentByIds(ctx context.Context, in *pb.DeleteTrainingStudentRequest) (*emptypb.Empty, error) {
	studentUuid := in.GetStudentUuid()
	trainingUuid := in.GetTrainingUuid()
	arg := db.DeleteTrainingStudentByIdsParams{
		TrainingUuid: pgtype.UUID{Bytes: uuid.MustParse(trainingUuid), Valid: true},
		StudentUuid:  pgtype.UUID{Bytes: uuid.MustParse(studentUuid), Valid: true},
	}

	err := ftc.trainingStudentService.DeleteTrainingStudentByIds(ctx, arg)
	if err != nil {
		return nil, err
	}

	return &emptypb.Empty{}, nil
}
