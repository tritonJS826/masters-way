package openapi

import (
	openapiTraining "mw-training/apiAutogenerated/training"
	"mw-training/internal/auth"
	"mw-training/internal/config"
	"net/http"
)

type authTransport struct {
	rt http.RoundTripper
}

func (t *authTransport) RoundTrip(req *http.Request) (*http.Response, error) {
	ctx := req.Context()
	if token, ok := ctx.Value(auth.ContextKeyAuthorization).(string); ok {
		req.Header.Set(auth.HeaderKeyAuthorization, token)
	}
	return t.rt.RoundTrip(req)
}

func MakeTrainingAPIClient(cfg *config.Config) *openapiTraining.APIClient {
	trainingAPIConfig := &openapiTraining.Configuration{
		Host:   cfg.TestTrainingAPIHost,
		Scheme: "http",
		Servers: openapiTraining.ServerConfigurations{
			{
				URL:         cfg.TestTrainingBaseURL,
				Description: "mw-training",
			},
		},
		HTTPClient: &http.Client{
			Transport: &authTransport{rt: http.DefaultTransport},
		},
	}
	return openapiTraining.NewAPIClient(trainingAPIConfig)
}
