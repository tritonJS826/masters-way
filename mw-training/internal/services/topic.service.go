package services

import (
	"context"
	db "mw-training/internal/db/sqlc"
	pb "mw-training/pkg/grpcAutoGenerated/training"

	"github.com/jackc/pgx/v5"
	"github.com/jackc/pgx/v5/pgtype"
	"github.com/jackc/pgx/v5/pgxpool"
)

type TopicRepository interface {
	CreateTopic(ctx context.Context, params db.CreateTopicInTrainingParams) (pb.Topic, error)
	UpdateTopic(ctx context.Context, params db.UpdateTopicParams) (pb.Topic, error)
	DeleteTopic(ctx context.Context, topicUuid pgtype.UUID) error
	WithTx(tx pgx.Tx) *db.Queries
}

type TopicService struct {
	topicRepository TopicRepository
	pgxPool         *pgxpool.Pool
}

func NewTopicService(pgxPool *pgxpool.Pool, topicRepository TopicRepository) *TopicService {
	return &TopicService{
		pgxPool:         pgxPool,
		topicRepository: topicRepository,
	}
}

func (ts *TopicService) CreateTopic(ctx context.Context, params db.CreateTopicInTrainingParams) (pb.Topic, error) {
	return ts.topicRepository.CreateTopic(ctx, params)
}

func (ts *TopicService) UpdateTopic(ctx context.Context, params db.UpdateTopicParams) (pb.Topic, error) {
	return ts.topicRepository.UpdateTopic(ctx, params)
}

func (ts *TopicService) DeleteTopic(ctx context.Context, topicUuid pgtype.UUID) error {
	return ts.topicRepository.DeleteTopic(ctx, topicUuid)
}
