package openapi

import (
	"mwmail/internal/auth"
	"mwmail/internal/config"
	"net/http"

	openapiMail "mwmail/apiAutogenerated/mail"
)

type authTransport struct {
	rt http.RoundTripper
}

func (t *authTransport) RoundTrip(req *http.Request) (*http.Response, error) {
	ctx := req.Context()
	if token, ok := ctx.Value(auth.ContextKeyAuthorization).(string); ok {
		req.Header.Set(auth.HeaderKeyAuthorization, token)
	}
	return t.rt.RoundTrip(req)
}

func MakeMailAPIClient(cfg *config.Config) *openapiMail.APIClient {
	chatAPIConfig := &openapiMail.Configuration{
		Host:   cfg.TestMailAPIHost,
		Scheme: "http",
		Servers: openapiMail.ServerConfigurations{
			{
				URL:         cfg.TestMailBaseURL,
				Description: "mw-mail",
			},
		},
		HTTPClient: &http.Client{
			Transport: &authTransport{rt: http.DefaultTransport},
		},
	}
	return openapiMail.NewAPIClient(chatAPIConfig)
}
