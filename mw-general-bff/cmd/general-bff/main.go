// TODO: move it to webhook on some controller
func autoGenerator(
	stopChan <-chan os.Signal,
	service *services.Service,
) {
	var lastSuccessTime time.Time
	var mutex sync.Mutex
	ticker := time.NewTicker(5 * time.Second)

	for {
		select {
		case <-stopChan:
			ticker.Stop()
			return
		case <-ticker.C:
			mutex.Lock()
			elapsed := time.Since(lastSuccessTime)
			mutex.Unlock()
			if elapsed < 5*time.Second {
				continue
			}
			ctx := context.WithValue(context.Background(), "messageToAi.Item.ContextKeyAuthorization", "Bearer "+"data.Item.token")
			messageToAi, err := service.TrainingService.GetMessageToAI(ctx)
			if err != nil {
				log.Printf("Error fetching template: %v", err)
				continue
			}

			if messageToAi == nil {
				log.Println("trainings.getTemplate returned nil. Retrying in 5 seconds... Remove this line")
				continue
			}

			generatedItemResponseRaw, err := service.GeneralService.AIChat(
				ctx,
				&schemas.AIChatPayload{Message: messageToAi.Message},
			)
			if err != nil {
				log.Printf("Error when generating: %v", err)
				generatedItemResponseRaw = &schemas.AIChatResponse{
					Message: "Error when generating...",
				}
				// continue
			}
			mutex.Lock()
			lastSuccessTime = time.Now()
			mutex.Unlock()

			generatedResponse := &pb.MessageFromAI{
				Uuid:            messageToAi.Uuid,
				MessageResponse: generatedItemResponseRaw.Message,
			}
			err = service.TrainingService.HandleResponseFromAiForItem(ctx, generatedResponse)
			if err != nil {
				log.Printf("Error when generating: %v", err)
				continue
			}
		}
	}
}

// @title     Masters way general-bff API
// @version 1.0
// @BasePath  /general
func main() {
	newConfig, err := config.LoadConfig("./")
	if err != nil {
		fmt.Fprintf(os.Stderr, "cannot load config: %v\n", err)
		os.Exit(1)
	}

	notificationConfig, err := grpc.NewClient(newConfig.NotificationAPIHost, grpc.WithTransportCredentials(insecure.NewCredentials()))
	if err != nil {
		fmt.Fprintf(os.Stderr, "Unable to create connection: %v", err)
		os.Exit(1)
	}
	defer notificationConfig.Close()

	trainingConfig, err := grpc.NewClient(newConfig.TrainingAPIHost, grpc.WithTransportCredentials(insecure.NewCredentials()))
	if err != nil {
		fmt.Fprintf(os.Stderr, "Unable to create connection: %v", err)
		os.Exit(1)
	}
	defer trainingConfig.Close()

	newService := services.NewService(&newConfig, notificationConfig, trainingConfig)
	newFacade := facades.NewFacade(newService, &newConfig)
	newController := controllers.NewController(newFacade)

	newRouter := routers.NewRouter(&newConfig, newController)
	newRouter.SetRoutes(&newConfig)

	newServer := &http.Server{
		Addr:    ":" + newConfig.ServerPort,
		Handler: newRouter.Gin,
	}

	// Start the server in a separate goroutine
	go func() {
		if err := newServer.ListenAndServe(); err != nil && err != http.ErrServerClosed {
			log.Fatalf("Server start failed: %s\n", err)
		}
	}()
	log.Println("Server started successfully")

	// Set up signal catching
	quit := make(chan os.Signal, 1)
	go autoGenerator(quit, newService)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)
	<-quit
	log.Println("Shutdown initiated...")
	close(quit)

	// Context for graceful shutdown with a timeout of 3 seconds
	ctx, cancel := context.WithTimeout(context.Background(), 3*time.Second)
	defer cancel()

	// Attempt to gracefully shutdown the server
	if err := newServer.Shutdown(ctx); err != nil {
		log.Printf("Server Shutdown Error: %v", err)
	}

	// Waiting for the shutdown context to be done or timeout
	select {
	case <-ctx.Done():
		log.Println("Server shutdown completed or timed out")
	}

	log.Println("Server exiting")
	os.Exit(0)
}
