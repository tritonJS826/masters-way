package controllers

import (
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/schemas"
	"mw-general-bff/internal/services"
	"mw-general-bff/pkg/utils"
	"net/http"

	"github.com/gin-gonic/gin"
)

// Without next lines swagger does not see openapi models
var _ = &openapiGeneral.MwserverInternalSchemasCreatePlanPayload{}

type PlanController struct {
	generalService *services.GeneralService
}

func NewPlanController(generalService *services.GeneralService) *PlanController {
	return &PlanController{generalService}
}

// Create Plan handler
// @Summary Create a new plan
// @Description
// @Tags plan
// @ID create-plan
// @Accept  json
// @Produce  json
// @Param request body schemas.CreatePlanPayload true "query params"
// @Success 200 {object} openapiGeneral.MwserverInternalSchemasCreatePlanPayload
// @Failure 403 {object} schemas.NoRightToChangeDayReportError "User doesn't have rights to create plan."
// @Router /plans [post]
func (pc *PlanController) CreatePlan(ctx *gin.Context) {
	var payload *schemas.CreatePlanPayload

	if err := ctx.ShouldBindJSON(&payload); err != nil {
		ctx.JSON(http.StatusBadRequest, gin.H{"status": "Failed payload", "error": err.Error()})
		return
	}

	response, err := pc.generalService.CreatePlan(ctx, payload)
	utils.HandleErrorGin(ctx, err)

	ctx.JSON(http.StatusOK, response)
}

// Update Plan handler
// @Summary Update plan by UUID
// @Description
// @Tags plan
// @ID update-plan
// @Accept  json
// @Produce  json
// @Param request body schemas.UpdatePlanPayload true "query params"
// @Param planId path string true "plan UUID"
// @Success 200 {object} openapiGeneral.MwserverInternalSchemasCreatePlanPayload
// @Failure 403 {object} schemas.NoRightToChangeDayReportError "User doesn't have rights to update plan."
// @Router /plans/{planId} [patch]
func (pc *PlanController) UpdatePlan(ctx *gin.Context) {
	var payload *schemas.UpdatePlanPayload
	planID := ctx.Param("planId")

	if err := ctx.ShouldBindJSON(&payload); err != nil {
		ctx.JSON(http.StatusBadRequest, gin.H{"status": "Failed payload", "error": err.Error()})
		return
	}

	plan, err := pc.generalService.UpdatePlan(ctx, &services.UpdatePlanParams{
		PlanID:      planID,
		Description: payload.Description,
		Time:        payload.Time,
		IsDone:      payload.IsDone,
	})
	utils.HandleErrorGin(ctx, err)

	ctx.JSON(http.StatusOK, plan)
}

// Delete Plan handler
// @Summary Delete plan by UUID
// @Description
// @Tags plan
// @ID delete-plan
// @Accept  json
// @Produce  json
// @Param planId path string true "plan UUID"
// @Success 204
// @Failure 403 {object} schemas.NoRightToChangeDayReportError "User doesn't have rights to delete plan."
// @Router /plans/{planId} [delete]
func (pc *PlanController) DeletePlanById(ctx *gin.Context) {
	planID := ctx.Param("planId")

	err := pc.generalService.DeletePlanById(ctx, planID)
	utils.HandleErrorGin(ctx, err)

	ctx.Status(http.StatusNoContent)
}
