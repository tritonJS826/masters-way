package controllers

import (
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/facades"
	"mw-general-bff/internal/schemas"
	"mw-general-bff/internal/services"
	"mw-general-bff/pkg/utils"
	"net/http"

	"github.com/gin-gonic/gin"
)

// Without next lines swagger does not see openapi models
var _ = &openapiGeneral.MwServerInternalSchemasProjectPopulatedResponse{}

type ProjectController struct {
	projectFacade *facades.ProjectFacade
}

func NewProjectController(projectFacade *facades.ProjectFacade) *ProjectController {
	return &ProjectController{projectFacade}
}

// Create Project handler
// @Summary Create a new project
// @Description
// @Tags project
// @ID create-project
// @Accept  json
// @Produce  json
// @Param request body schemas.CreateProjectPayload true "query params"
// @Success 200 {object} openapiGeneral.MwServerInternalSchemasProjectPopulatedResponse
// @Router /projects [post]
func (pc *ProjectController) CreateProject(ctx *gin.Context) {
	var payload *schemas.CreateProjectPayload

	if err := ctx.ShouldBindJSON(&payload); err != nil {
		ctx.JSON(http.StatusBadRequest, gin.H{"status": "Failed payload", "error": err.Error()})
		return
	}

	response, err := pc.projectFacade.CreateProject(ctx, payload)
	utils.HandleErrorGin(ctx, err)

	ctx.JSON(http.StatusOK, response)
}

// Update Project handler
// @Summary Update project by id
// @Description
// @Tags project
// @ID update-project
// @Accept  json
// @Produce  json
// @Param request body schemas.UpdateProjectPayload true "query params"
// @Param projectId path string true "project id"
// @Success 200 {object} openapiGeneral.MwServerInternalSchemasProjectPopulatedResponse
// @Router /projects/{projectId} [patch]
func (pc *ProjectController) UpdateProject(ctx *gin.Context) {
	var payload *schemas.UpdateProjectPayload
	projectID := ctx.Param("projectId")

	if err := ctx.ShouldBindJSON(&payload); err != nil {
		ctx.JSON(http.StatusBadRequest, gin.H{"status": "Failed payload", "error": err.Error()})
		return
	}

	response, err := pc.projectFacade.UpdateProject(ctx, &services.UpdateProjectParams{
		ID:        projectID,
		Name:      payload.Name,
		IsPrivate: payload.IsPrivate,
	})
	utils.HandleErrorGin(ctx, err)

	ctx.JSON(http.StatusOK, response)
}

// Get Project handler
// @Summary Get project by id
// @Description
// @Tags project
// @ID get-project
// @Accept  json
// @Produce  json
// @Param projectId path string true "project id"
// @Success 200 {object} openapiGeneral.MwServerInternalSchemasProjectPopulatedResponse
// @Router /projects/{projectId} [get]
func (pc *ProjectController) GetProjectByID(ctx *gin.Context) {
	projectID := ctx.Param("projectId")

	response, err := pc.projectFacade.GetProjectByID(ctx, projectID)
	utils.HandleErrorGin(ctx, err)

	ctx.JSON(http.StatusOK, response)
}

// Delete Project handler
// @Summary Delete project by id
// @Description
// @Tags project
// @ID delete-project
// @Accept  json
// @Produce  json
// @Param projectId path string true "project id"
// @Success 204
// @Router /projects/{projectId} [delete]
func (pc *ProjectController) DeleteProject(ctx *gin.Context) {
	projectID := ctx.Param("projectId")

	err := pc.projectFacade.DeleteProjectByID(ctx, projectID)
	utils.HandleErrorGin(ctx, err)

	ctx.Status(http.StatusNoContent)
}
