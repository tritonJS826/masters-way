package facades

import (
	"context"
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/schemas"
	"mw-general-bff/internal/services"
)

type ProblemFacade struct {
	generalService *services.GeneralService
}

func newProblemFacade(generalService *services.GeneralService) *ProblemFacade {
	return &ProblemFacade{generalService}
}

func (gf *ProblemFacade) CreateProblem(ctx context.Context, payload *schemas.CreateProblemPayload) (*openapiGeneral.MwServerInternalSchemasProblemPopulatedResponse, error) {
	return gf.generalService.CreateProblem(ctx, payload)
}

func (gf *ProblemFacade) UpdateProblem(ctx context.Context, params *services.UpdateProblemParams) (*openapiGeneral.MwServerInternalSchemasProblemPopulatedResponse, error) {
	return gf.generalService.UpdateProblem(ctx, params)
}

func (gf *ProblemFacade) DeleteProblemById(ctx context.Context, problemID string) error {
	return gf.generalService.DeleteProblemById(ctx, problemID)
}
