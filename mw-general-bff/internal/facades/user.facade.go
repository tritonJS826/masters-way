package facades

import (
	"context"
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/services"

	"github.com/google/uuid"
)

type UserFacade struct {
	generalService *services.GeneralService
}

func newUserFacade(generalService *services.GeneralService) *UserFacade {
	return &UserFacade{generalService}
}

func (gs *UserFacade) UpdateUser(ctx context.Context, params *services.UpdateUserParams) (*openapiGeneral.MwServerInternalSchemasUserPlainResponse, error) {
	return gs.generalService.UpdateUser(ctx, params)
}

func (gs *UserFacade) GetAllUsers(ctx context.Context, params *services.GetAllUsersParams) (*openapiGeneral.MwServerInternalSchemasGetAllUsersResponse, error) {
	return gs.generalService.GetAllUsers(ctx, params)
}

func (gs *UserFacade) GetPopulatedUserById(ctx context.Context, userUuid uuid.UUID) (*openapiGeneral.MwServerInternalSchemasUserPopulatedResponse, error) {
	return gs.generalService.GetPopulatedUserById(ctx, userUuid)
}
