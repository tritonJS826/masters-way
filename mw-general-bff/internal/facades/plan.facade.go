package facades

import (
	"context"
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/schemas"
	"mw-general-bff/internal/services"
)

type PlanFacade struct {
	generalService *services.GeneralService
}

func newPlanFacade(generalService *services.GeneralService) *PlanFacade {
	return &PlanFacade{generalService}
}

func (gs *PlanFacade) CreatePlan(ctx context.Context, payload *schemas.CreatePlanPayload) (*openapiGeneral.MwServerInternalSchemasPlanPopulatedResponse, error) {
	return gs.generalService.CreatePlan(ctx, payload)
}

func (gs *PlanFacade) UpdatePlan(ctx context.Context, params *services.UpdatePlanParams) (*openapiGeneral.MwServerInternalSchemasPlanPopulatedResponse, error) {
	return gs.generalService.UpdatePlan(ctx, params)
}

func (gs *PlanFacade) DeletePlanById(ctx context.Context, planID string) error {
	return gs.generalService.DeletePlanById(ctx, planID)
}
