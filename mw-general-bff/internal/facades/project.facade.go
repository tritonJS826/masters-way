package facades

import (
	"context"
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/schemas"
	"mw-general-bff/internal/services"
)

type ProjectFacade struct {
	generalService *services.GeneralService
}

func newProjectFacade(generalService *services.GeneralService) *ProjectFacade {
	return &ProjectFacade{generalService}
}

func (gs *ProjectFacade) CreateProject(ctx context.Context, payload *schemas.CreateProjectPayload) (*openapiGeneral.MwServerInternalSchemasProjectPopulatedResponse, error) {
	return gs.generalService.CreateProject(ctx, payload)
}

func (gs *ProjectFacade) UpdateProject(ctx context.Context, params *services.UpdateProjectParams) (*openapiGeneral.MwServerInternalSchemasProjectPopulatedResponse, error) {
	return gs.generalService.UpdateProject(ctx, params)
}

func (gs *ProjectFacade) GetProjectByID(ctx context.Context, projectID string) (*openapiGeneral.MwServerInternalSchemasProjectPopulatedResponse, error) {
	return gs.generalService.GetProjectByID(ctx, projectID)
}

func (gs *ProjectFacade) DeleteProjectByID(ctx context.Context, projectID string) error {
	return gs.generalService.DeleteProjectByID(ctx, projectID)
}
