package facades

import (
	"context"
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/schemas"
	"mw-general-bff/internal/services"
)

type MetricFacade struct {
	generalService *services.GeneralService
}

func newMetricFacade(generalService *services.GeneralService) *MetricFacade {
	return &MetricFacade{generalService}
}

func (gs *MetricFacade) CreateMetric(ctx context.Context, payload *schemas.CreateMetricPayload) (*openapiGeneral.MwServerInternalSchemasMetricResponse, error) {
	return gs.generalService.CreateMetric(ctx, payload)
}

func (gs *MetricFacade) UpdateMetric(ctx context.Context, params *services.UpdateMetricParams) (*openapiGeneral.MwServerInternalSchemasMetricResponse, error) {
	return gs.generalService.UpdateMetric(ctx, params)
}

func (gs *MetricFacade) DeleteMetricById(ctx context.Context, metricID string) error {
	return gs.generalService.DeleteMetricById(ctx, metricID)
}
