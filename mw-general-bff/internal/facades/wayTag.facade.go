package facades

import (
	"context"
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/services"
)

type WayTagFacade struct {
	generalService *services.GeneralService
}

func newWayTagFacade(generalService *services.GeneralService) *WayTagFacade {
	return &WayTagFacade{generalService}
}

func (wf *WayTagFacade) AddWayTagToWay(ctx context.Context, name, wayID string) (*openapiGeneral.MwServerInternalSchemasWayTagResponse, error) {
	return wf.generalService.AddWayTagToWay(ctx, name, wayID)
}

func (wf *WayTagFacade) DeleteWayTagFromWayByTagID(ctx context.Context, wayTagID, wayID string) error {
	return wf.generalService.DeleteWayTagFromWayByTagID(ctx, wayTagID, wayID)
}
