package facades

import (
	"context"
	openapiGeneral "mw-general-bff/apiAutogenerated/general"
	"mw-general-bff/internal/schemas"
	"mw-general-bff/internal/services"
)

type WayCollectionFacade struct {
	generalService *services.GeneralService
}

func newWayCollectionFacade(generalService *services.GeneralService) *WayCollectionFacade {
	return &WayCollectionFacade{generalService}
}

func (wf *WayCollectionFacade) CreateWayCollection(ctx context.Context, payload *schemas.CreateWayCollectionPayload) (*openapiGeneral.MwServerInternalSchemasWayCollectionPopulatedResponse, error) {
	return wf.generalService.CreateWayCollection(ctx, payload)
}

func (wf *WayCollectionFacade) UpdateWayCollection(ctx context.Context, wayCollectionID, wayCollectionName string) (*openapiGeneral.MwServerInternalSchemasWayCollectionPlainResponse, error) {
	return wf.generalService.UpdateWayCollection(ctx, wayCollectionID, wayCollectionName)
}

func (wf *WayCollectionFacade) DeleteWayCollectionById(ctx context.Context, wayCollectionID string) error {
	return wf.generalService.DeleteWayCollectionById(ctx, wayCollectionID)
}
