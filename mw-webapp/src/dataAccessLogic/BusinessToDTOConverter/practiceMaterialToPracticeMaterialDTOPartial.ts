import {MwTrainingBffInternalSchemasUpdatePracticeMaterialPayload} from "src/apiAutogenerated/training";
import {deleteUndefinedFields} from "src/dataAccessLogic/BusinessToDTOConverter/deleteUndefinedFields";
import {PracticeMaterial} from "src/model/businessModel/PracticeMaterial";
import {PartialWithUuid} from "src/utils/PartialWithUuid";

/**
 * Convert {@link practiceMaterial} to {@link MwTrainingBffInternalSchemasUpdatePracticeMaterialPayload}
 */
export const practiceMaterialToPracticeMaterialDTOPartial = (
  practiceMaterial: PartialWithUuid<PracticeMaterial>,
): MwTrainingBffInternalSchemasUpdatePracticeMaterialPayload => {
  const practiceMaterialPartialDTO: MwTrainingBffInternalSchemasUpdatePracticeMaterialPayload = {
    name: practiceMaterial.name,
    taskDescription: practiceMaterial.taskDescription,
    answer: practiceMaterial.answer,
    practiceType: practiceMaterial.practiceType,
    timeToAnswer: practiceMaterial.timeToAnswer,
  };

  const preparedPracticeMaterialPartialDTO = deleteUndefinedFields(practiceMaterialPartialDTO);

  return preparedPracticeMaterialPartialDTO;
};
