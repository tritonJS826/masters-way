import {MwTrainingBffInternalSchemasUpdateTrainingPayload} from "src/apiAutogenerated/training";
import {deleteUndefinedFields} from "src/dataAccessLogic/BusinessToDTOConverter/deleteUndefinedFields";
import {Training} from "src/model/businessModel/Training";
import {PartialWithUuid} from "src/utils/PartialWithUuid";

/**
 * Convert {@link training} to {@link SchemasUpdateWayPayload}
 */
export const trainingToTrainingDTOPartial = (
  training: PartialWithUuid<Training>,
): MwTrainingBffInternalSchemasUpdateTrainingPayload => {
  const trainingPartialDTO: MwTrainingBffInternalSchemasUpdateTrainingPayload = {
    name: training.name,
    description: training.description,
    isPrivate: training.isPrivate,
  };

  const preparedTrainingPartialDTO = deleteUndefinedFields(trainingPartialDTO);

  return preparedTrainingPartialDTO;
};
