import {SchemasMetricResponse} from "src/apiAutogenerated/general";
import {Metric} from "src/model/businessModel/Metric";

/**
 * Convert {@link metric} to {@link SchemasMetricResponse}
 */
export const metricToMetricDTO = (metric: Metric): SchemasMetricResponse => {
  const metricDTO: SchemasMetricResponse = {
    ...metric,
    estimationTime: 0,
    doneDate: metric.doneDate?.toISOString() ?? null,
  };

  return metricDTO;
};
