import {SchemasUpdateProjectPayload} from "src/apiAutogenerated/general";
import {deleteUndefinedFields} from "src/dataAccessLogic/BusinessToDTOConverter/deleteUndefinedFields";
import {Project} from "src/model/businessModel/Project";
import {PartialWithUuid} from "src/utils/PartialWithUuid";

/**
 * Convert {@link project} to {@link SchemasUpdateWayPayload}
 */
export const projectToProjectDTOPartial = (project: PartialWithUuid<Project>): SchemasUpdateProjectPayload => {
  const projectPartialDTO: SchemasUpdateProjectPayload = {
    name: project.name,
    isPrivate: project.isPrivate,
  };

  const preparedProjectPartialDTO = deleteUndefinedFields(projectPartialDTO);

  return preparedProjectPartialDTO;
};
