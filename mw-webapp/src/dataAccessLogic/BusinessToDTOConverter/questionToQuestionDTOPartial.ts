import {MwTrainingBffInternalSchemasUpdateQuestionPayload} from "src/apiAutogenerated/training";
import {deleteUndefinedFields} from "src/dataAccessLogic/BusinessToDTOConverter/deleteUndefinedFields";
import {Question} from "src/model/businessModel/Test";
import {PartialWithUuid} from "src/utils/PartialWithUuid";

/**
 * Convert {@link question} to {@link MwTrainingBffInternalSchemasUpdateQuestionPayload}
 */
export const questionToQuestionDTOPartial = (
  question: PartialWithUuid<Question>,
): MwTrainingBffInternalSchemasUpdateQuestionPayload => {
  const questionPartialDTO: MwTrainingBffInternalSchemasUpdateQuestionPayload = {
    answer: question.answer,
    // PracticeType: question.
    order: question.order,
    questionText: question.questionText,
    timeToAnswer: question.timeToAnswer,
    name: question.name,
  };

  const preparedQuestionPartialDTO = deleteUndefinedFields(questionPartialDTO);

  return preparedQuestionPartialDTO;
};

