import {MwServerInternalSchemasJobDonePopulatedResponse} from "src/apiAutogenerated/general";
import {JobDone} from "src/model/businessModel/JobDone";
import {Label} from "src/model/businessModel/Label";

/**
 * Convert {@link JobDoneDTo} to {@link JobDone}
 */
export const jobDoneDTOToJobDone = (jobDoneDTO: MwServerInternalSchemasJobDonePopulatedResponse): JobDone => {
  return new JobDone({
    ...jobDoneDTO,
    createdAt: new Date(jobDoneDTO.createdAt),
    updatedAt: new Date(jobDoneDTO.updatedAt),
    tags: jobDoneDTO.tags.map(label => new Label(label)),
  });
};
