import {MwTrainingBffInternalSchemasTrainingPreview} from "src/apiAutogenerated/training";
import {trainingTagDTOToTrainingTag} from "src/dataAccessLogic/DTOToPreviewConverter/trainingTagDTOToTrainingTag";
import {TrainingPreview} from "src/model/businessModelPreview/TrainingPreview";

/**
 * Convert {@link trainingPreviewDTO} to {@link TrainingPreview}
 */
export const trainingPreviewDTOToTrainingPreview = (
  trainingPreviewDTO: MwTrainingBffInternalSchemasTrainingPreview,
): TrainingPreview => {
  return new TrainingPreview({
    ...trainingPreviewDTO,
    trainingTags: trainingPreviewDTO.trainingTag.map(trainingTagDTOToTrainingTag),
    createdAt: new Date(trainingPreviewDTO.createdAt),
    updatedAt: new Date(trainingPreviewDTO.updatedAt),
  });
};
