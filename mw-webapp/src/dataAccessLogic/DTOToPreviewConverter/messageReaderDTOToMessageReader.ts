import {SchemasMessageReader} from "src/apiAutogenerated/chat";
import {MessageReader} from "src/model/businessModel/Message";

/**
 * Convert {@link messageReaderDTO} to {@link MessageReader}
 */
export const messageReaderDTOToMessageReader = (messageReaderDTO: SchemasMessageReader): MessageReader => {
  return new MessageReader({
    ...messageReaderDTO,
    readDate: new Date(messageReaderDTO.readDate),
  });
};
