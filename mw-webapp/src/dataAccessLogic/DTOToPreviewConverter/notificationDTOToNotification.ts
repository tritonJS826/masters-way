import {MwNotificationBffInternalSchemasNotificationResponse} from "src/apiAutogenerated/notification";
import {Notification} from "src/model/businessModel/Notification";

/**
 * Convert {@link notificationDTO} to {@link Notification}
 */
export const notificationDTOToNotification = (
  notificationDTO: MwNotificationBffInternalSchemasNotificationResponse,
): Notification => {
  return new Notification({
    description: notificationDTO.description,
    id: notificationDTO.uuid,
    isRead: notificationDTO.isRead,
    title: notificationDTO.description,
  });
};
