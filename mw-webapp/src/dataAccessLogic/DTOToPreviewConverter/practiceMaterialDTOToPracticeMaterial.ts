import {MwTrainingBffInternalSchemasPracticeMaterial} from "src/apiAutogenerated/training";
import {PracticeMaterial} from "src/model/businessModel/PracticeMaterial";

/**
 * Convert {@link practiceMaterialDTO} to {@link PracticeMaterial}
 */
export const practiceMaterialDTOToPracticeMaterial = (
  practiceMaterialDTO: MwTrainingBffInternalSchemasPracticeMaterial,
): PracticeMaterial => {
  return new PracticeMaterial({
    ...practiceMaterialDTO,
    createdAt: new Date(practiceMaterialDTO.createdAt),
    updatedAt: new Date(practiceMaterialDTO.updatedAt),
  });
};
