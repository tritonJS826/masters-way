import {SchemasWayStatisticsTriplePeriod} from "src/apiAutogenerated/general";
import {WayStatistics, WayStatisticsTriple} from "src/model/businessModel/WayStatistics";

/**
 * Convert {@link SchemasWayStatistics} to {@link WayStatistics}
 */
export const wayStatisticsDTOToWayStatistics = (
  wayStatisticsTripleDTO: SchemasWayStatisticsTriplePeriod,
): WayStatisticsTriple => {
  const wayStatistics = new WayStatisticsTriple({
    lastMonth: new WayStatistics(wayStatisticsTripleDTO.lastMonth),
    lastWeek: new WayStatistics(wayStatisticsTripleDTO.lastWeek),
    totalPeriod: new WayStatistics(wayStatisticsTripleDTO.totalTime),
  });

  return wayStatistics;
};
