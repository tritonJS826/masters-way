import {SchemasRoomPopulatedResponse} from "src/apiAutogenerated/chat";
import {ChatGroup} from "src/model/businessModel/ChatGroup";

/**
 * Convert {@link chatGroupDTO} to {@link ChatGroup}
 */
export const chatGroupDTOToChatGroup = (chatGroupDTO: SchemasRoomPopulatedResponse): ChatGroup => {
  return new ChatGroup({
    ...chatGroupDTO,
    src: null,
  });
};
