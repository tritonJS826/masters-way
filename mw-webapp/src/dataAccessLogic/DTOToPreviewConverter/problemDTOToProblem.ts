import {SchemasProblemPopulatedResponse} from "src/apiAutogenerated/general";
import {Problem} from "src/model/businessModel/Problem";

/**
 * Convert {@link ProblemDTO} to {@link Problem}
 */
export const problemDTOToProblem = (problemDTO: SchemasProblemPopulatedResponse): Problem => {
  return new Problem({
    ...problemDTO,
    createdAt: new Date(problemDTO.createdAt),
    updatedAt: new Date(problemDTO.updatedAt),
  });
};
