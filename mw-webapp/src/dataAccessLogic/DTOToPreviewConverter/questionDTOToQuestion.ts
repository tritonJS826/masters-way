import {MwTrainingBffInternalSchemasQuestion} from "src/apiAutogenerated/training";
import {Question} from "src/model/businessModel/Test";

/**
 * Convert {@link questionDTO} to {@link questionResult}
 */
export const questionDTOToQuestion = (questionDTO: MwTrainingBffInternalSchemasQuestion): Question => {
  return new Question({
    ...questionDTO,
    createdAt: new Date(questionDTO.createdAt),
    updatedAt: new Date(questionDTO.updatedAt),
  });
};
