import {MwTrainingBffInternalSchemasTopicPreview} from "src/apiAutogenerated/training";
import {TopicPreview} from "src/model/businessModelPreview/TopicPreview";

/**
 * Convert {@link topicPreviewDTO} to {@link TopicPreview}
 */
export const topicPreviewDTOToTopicPreview = (
  topicPreviewDTO: MwTrainingBffInternalSchemasTopicPreview,
): TopicPreview => {
  return new TopicPreview({
    ...topicPreviewDTO,
    createdAt: new Date(topicPreviewDTO.createdAt),
    children: [],
  });
};
