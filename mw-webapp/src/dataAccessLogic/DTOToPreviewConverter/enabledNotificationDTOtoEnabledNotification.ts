import {MwNotificationBffInternalSchemasEnabledNotificationResponse} from "src/apiAutogenerated/notification";
import {EnabledNotification} from "src/model/businessModel/EnabledNotification";

/**
 * Convert {@link chatDTO} to {@link Room}
 */
export const enabledNotificationDTOtoEnabledNotification = (
  enabledNotificationDTO: MwNotificationBffInternalSchemasEnabledNotificationResponse,
): EnabledNotification => {
  return new EnabledNotification({
    channel: enabledNotificationDTO.channel,
    isEnabled: enabledNotificationDTO.isEnabled,
    nature: enabledNotificationDTO.nature,
    uuid: enabledNotificationDTO.uuid,
  });
};
