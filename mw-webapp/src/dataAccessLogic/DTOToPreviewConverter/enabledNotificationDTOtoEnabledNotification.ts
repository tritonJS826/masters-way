import {MwNotificationBffInternalSchemasNotificationSettingResponse} from "src/apiAutogenerated/notification";
import {NotificationSetting} from "src/model/businessModel/EnabledNotification";

/**
 * Convert {@link chatDTO} to {@link Room}
 */
export const enabledNotificationDTOtoEnabledNotification = (
  enabledNotificationDTO: MwNotificationBffInternalSchemasNotificationSettingResponse,
): NotificationSetting => {
  return new NotificationSetting({
    channel: enabledNotificationDTO.channel,
    isEnabled: enabledNotificationDTO.isEnabled,
    nature: enabledNotificationDTO.nature,
    uuid: enabledNotificationDTO.uuid,
  });
};
