import {MwChatBffInternalSchemasRoomPreviewResponse} from "src/apiAutogenerated/chat";
import {ChatPreview} from "src/model/businessModelPreview/ChatPreview";

/**
 * Convert {@link chatPreviewDTO} to {@link ChatPreview}
 */
export const chatPreviewDTOToChatPreview = (chatPreviewDTO: MwChatBffInternalSchemasRoomPreviewResponse): ChatPreview => {
  return new ChatPreview({
    ...chatPreviewDTO,
    participantIds: chatPreviewDTO.users.map((user) => user.userId),
  });
};
