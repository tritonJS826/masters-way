import {MwserverInternalSchemasWayStatisticsTriplePeriod} from "src/apiAutogenerated/general";
import {wayStatisticsDTOToWayStatistics} from "src/dataAccessLogic/DTOToPreviewConverter/wayStatisticsDTOToWayStatistics";
import {WayStatisticsTriple} from "src/model/businessModel/WayStatistics";

/**
 * Convert {@link SchemasWayStatisticsTriplePeriod} to {@link WayStatisticsTriple}
 */
export const wayStatisticsTripleDTOToWayStatisticsTriple = (
  wayStatisticsTripleDTO: MwserverInternalSchemasWayStatisticsTriplePeriod,
): WayStatisticsTriple => {
  const wayStatisticsTriple = new WayStatisticsTriple({
    lastMonth: wayStatisticsDTOToWayStatistics(wayStatisticsTripleDTO.lastMonth),
    lastWeek: wayStatisticsDTOToWayStatistics(wayStatisticsTripleDTO.lastWeek),
    totalPeriod: wayStatisticsDTOToWayStatistics(wayStatisticsTripleDTO.totalTime),
  });

  return wayStatisticsTriple;
};
