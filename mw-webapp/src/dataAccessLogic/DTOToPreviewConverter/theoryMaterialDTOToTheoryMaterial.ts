import {MwTrainingBffInternalSchemasTheoryMaterial} from "src/apiAutogenerated/training";
import {TheoryMaterial} from "src/model/businessModel/TheoryMaterial";

/**
 * Convert {@link theoryMaterialDTO} to {@link PracticeMaterial}
 */
export const theoryMaterialDTOToTheoryMaterial = (
  theoryMaterialDTO: MwTrainingBffInternalSchemasTheoryMaterial,
): TheoryMaterial => {
  return new TheoryMaterial({
    ...theoryMaterialDTO,
    createdAt: new Date(theoryMaterialDTO.createdAt),
    updatedAt: new Date(theoryMaterialDTO.updatedAt),
  });
};
