import {MwTrainingBffInternalSchemasTest} from "src/apiAutogenerated/training";
import {questionDTOToQuestion} from "src/dataAccessLogic/DTOToPreviewConverter/questionDTOToQuestion";
import {Test} from "src/model/businessModel/Test";

/**
 * Convert {@link TestDTO} to {@link Test}
 */
export const testDTOToTest = (testDTO: MwTrainingBffInternalSchemasTest): Test => {
  return new Test({
    ...testDTO,
    createdAt: new Date(testDTO.createdAt),
    updatedAt: new Date(testDTO.updatedAt),
    questions: testDTO.questions.map(questionDTOToQuestion),
  });
};
