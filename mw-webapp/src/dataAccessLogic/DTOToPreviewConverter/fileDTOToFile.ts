import {MwChatBffInternalSchemasUploadFileResponse} from "src/apiAutogenerated/chat";
import {FileModel} from "src/model/businessModel/File";

/**
 * Convert {@link chatDTO} to {@link Room}
 */
export const fileDTOToFile = (fileDTO: MwChatBffInternalSchemasUploadFileResponse): FileModel => {
  return new FileModel({
    ...fileDTO,
    uuid: fileDTO.id,
    url: fileDTO.srcUrl,
  });
};
