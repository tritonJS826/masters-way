import {MwTrainingBffInternalSchemasTopic} from "src/apiAutogenerated/training";
import {TrainingTopic} from "src/model/businessModel/Training";

/**
 * Convert {@link topicDTO} to {@link TrainingTopic}
 */
export const trainingTopicDTOToTrainingTopic = (
  topicDTO: MwTrainingBffInternalSchemasTopic,
): TrainingTopic => {
  return new TrainingTopic({
    ...topicDTO,
    createdAt: new Date(topicDTO.createdAt),
    children: [],
  });
};
