import {MwServerInternalSchemasProjectPlainResponse} from "src/apiAutogenerated/general";
import {ProjectPreview} from "src/model/businessModelPreview/ProjectPreview";

/**
 * Convert {@link SchemasProjectPlainResponse} to {@link ProjectPreview}
 */
export const projectPlainDTOToProjectPreview = (projectDTO: MwServerInternalSchemasProjectPlainResponse): ProjectPreview => {
  return new ProjectPreview({
    ...projectDTO,
    uuid: projectDTO.id,
  });
};
