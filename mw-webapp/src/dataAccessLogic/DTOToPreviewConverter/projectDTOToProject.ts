import {MwserverInternalSchemasProjectPopulatedResponse} from "src/apiAutogenerated/general";
import {userDTOToUserNotSaturatedWayConverter}
  from "src/dataAccessLogic/DTOToPreviewConverter/userDTOToUserNotSaturatedWayConverter";
import {wayDTOToWayPreview} from "src/dataAccessLogic/DTOToPreviewConverter/wayDTOToWayPreview";
import {Project} from "src/model/businessModel/Project";

/**
 * Convert {@link SchemasProjectPopulatedResponse} to {@link Project}
 */
export const projectDTOToProject = (projectDTO: MwserverInternalSchemasProjectPopulatedResponse): Project => {
  return new Project({
    ...projectDTO,
    uuid: projectDTO.id,
    ownerId: projectDTO.ownerId,
    users: projectDTO.users.map(userDTOToUserNotSaturatedWayConverter),
    ways: projectDTO.ways.map(wayDTOToWayPreview),
  });
};
