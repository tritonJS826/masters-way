import {DeleteUserTagRequest} from "src/apiAutogenerated/general";
import {UserTag} from "src/model/businessModel/User";
import {UserTagService} from "src/service/UserTagService";

/**
 * Params for{@link UserTagDAL.createUserTag}
 */
interface CreateUserTagParams {

  /**
   * User tag name
   */
  name: string;

  /**
   * Owner uuid
   */
  ownerUuid: string;
}

/**
 * Provides methods to interact with the user tags
 */
export class UserTagDAL {

  /**
   * Create user tag
   */
  public static async createUserTag(params: CreateUserTagParams): Promise<UserTag> {
    const userTagDTO = await UserTagService.createUserTag({request: params});

    const userTag = new UserTag({...userTagDTO});

    return userTag;
  }

  /**
   * Delete user tag by UUID
   */
  public static async deleteUserTag(params: DeleteUserTagRequest): Promise<void> {
    await UserTagService.deleteUserTag(params);
  }

}
