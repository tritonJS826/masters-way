import {DeleteUserTagRequest} from "src/apiAutogenerated/general";
import {Skill} from "src/model/businessModel/User";
import {SkillService} from "src/service/SkillService";

/**
 * Params for{@link SkillDAL.createSkill}
 */
interface CreateSkillParams {

  /**
   * Skill name
   */
  name: string;

  /**
   * Owner uuid
   */
  ownerUuid: string;
}

/**
 * Provides methods to interact with the skills
 */
export class SkillDAL {

  /**
   * Create skill
   */
  public static async createSkill(params: CreateSkillParams): Promise<Skill> {
    const skillDTO = await SkillService.createSkill({request: params});

    const skill = new Skill({...skillDTO});

    return skill;
  }

  /**
   * Delete skill by UUID
   */
  public static async deleteSkill(params: DeleteUserTagRequest): Promise<void> {
    await SkillService.deleteSkill(params);
  }

}
