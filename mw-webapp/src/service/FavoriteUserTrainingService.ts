import {
  CreateFavoriteUserTrainingRequest,
  DeleteFavoriteUserTrainingRequest,
} from "src/apiAutogenerated/training";
import {favoriteUserTrainingService} from "src/service/services";

/**
 * Provides methods to interact with the FavoriteUserTrainings
 */
export class FavoriteUserTrainingService {

  /**
   * Create favorite user training
   */
  public static async createFavoriteUserTraining(requestParameters: CreateFavoriteUserTrainingRequest): Promise<void> {
    await favoriteUserTrainingService.createFavoriteUserTraining(requestParameters);
  }

  /**
   * Delete favorite user training
   */
  public static async deleteFavoriteUserTraining(requestParameters: DeleteFavoriteUserTrainingRequest): Promise<void> {
    await favoriteUserTrainingService.deleteFavoriteUserTraining(requestParameters);
  }

}
