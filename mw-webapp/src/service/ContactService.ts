import {
  CreateUserContactRequest,
  DeleteUserContactRequest,
  MwGeneralBffInternalSchemasUserContact,
  UpdateUserContactRequest,
} from "src/apiAutogenerated/general";
import {createContactService, updateDeleteContactService} from "src/service/services";

/**
 * Provides methods to interact with the contacts
 */
export class ContactService {

  /**
   * Create contact
   */
  public static async createContact(
    requestParameters: CreateUserContactRequest,
  ): Promise<MwGeneralBffInternalSchemasUserContact> {
    const contact = await createContactService.createUserContact(requestParameters);

    return contact;
  }

  /**
   * Update contact
   */
  public static async updateContact(
    requestParameters: UpdateUserContactRequest,
  ): Promise<MwGeneralBffInternalSchemasUserContact> {
    const updatedContact = await updateDeleteContactService.updateUserContact(requestParameters);

    return updatedContact;
  }

  /**
   * Delete contact by UUID
   */
  public static async deleteContact(requestParameters: DeleteUserContactRequest): Promise<void> {
    await updateDeleteContactService.deleteUserContact(requestParameters);
  }

}
