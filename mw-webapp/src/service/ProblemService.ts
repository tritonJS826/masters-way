import {
  CreateProblemRequest,
  DeleteProblemRequest,
  SchemasProblemPopulatedResponse,
  UpdateProblemRequest,
} from "src/apiAutogenerated";
import {problemService} from "src/service/services";

/**
 * Provides methods to interact with the problems
 */
export class ProblemService {

  /**
   * Create problem
   */
  public static async createProblem(requestParameters: CreateProblemRequest): Promise<SchemasProblemPopulatedResponse> {
    const problem = await problemService.createProblem(requestParameters);

    return problem;
  }

  /**
   * Update problem
   */
  public static async updateProblem(requestParameters: UpdateProblemRequest): Promise<SchemasProblemPopulatedResponse> {
    const updatedProblem = await problemService.updateProblem(requestParameters);

    return updatedProblem;
  }

  /**
   * Delete problem by UUID
   */
  public static async deleteProblem(requestParameters: DeleteProblemRequest): Promise<void> {
    await problemService.deleteProblem(requestParameters);
  }

}
