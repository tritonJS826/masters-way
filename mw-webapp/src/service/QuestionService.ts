import {
  CreateQuestionRequest,
  DeleteQuestionRequest,
  MwTrainingBffInternalSchemasQuestion,
  UpdateQuestionRequest,
} from "src/apiAutogenerated/training";
import {questionService} from "src/service/services";

/**
 * Provides methods to interact with the Questions
 */
export class QuestionService {

  /**
   * Create question
   */
  public static async createQuestion(requestParameters: CreateQuestionRequest): Promise<MwTrainingBffInternalSchemasQuestion> {
    const question = await questionService.createQuestion(requestParameters);

    return question;
  }

  /**
   * Update question
   */
  public static async updateQuestion(requestParameters: UpdateQuestionRequest): Promise<MwTrainingBffInternalSchemasQuestion> {
    const updatedQuestion = await questionService.updateQuestion(requestParameters);

    return updatedQuestion;
  }

  /**
   * Delete question
   */
  public static async deleteQuestion(requestParameters: DeleteQuestionRequest): Promise<void> {
    await questionService.deleteQuestion(requestParameters);
  }

}

