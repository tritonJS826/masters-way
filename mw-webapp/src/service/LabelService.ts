import {
  CreateLabelRequest,
  DeleteLabelRequest,
  SchemasLabelResponse,
  UpdateLabelRequest,
} from "src/apiAutogenerated/general";
import {labelService} from "src/service/services";

/**
 * Provides methods to interact with the labels
 */
export class LabelService {

  /**
   * Create label
   */
  public static async createLabel(requestParameters: CreateLabelRequest): Promise<SchemasLabelResponse> {
    const jobTag = await labelService.createLabel(requestParameters);

    return jobTag;
  }

  /**
   * Update label
   */
  public static async updateLabel(requestParameters: UpdateLabelRequest): Promise<SchemasLabelResponse> {
    const updatedJobTag = await labelService.updateLabel(requestParameters);

    return updatedJobTag;
  }

  /**
   * Delete label by UUID
   */
  public static async deleteLabel(requestParameters: DeleteLabelRequest): Promise<void> {
    await labelService.deleteLabel(requestParameters);
  }

}
