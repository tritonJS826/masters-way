import {
  MwNotificationBffInternalSchemasEnabledNotificationResponse,
  MwNotificationBffInternalSchemasGetEnabledNotificationListResponse,
} from "src/apiAutogenerated/notification";
import {enabledNotificationService} from "src/service/services";

/**
 * Provides methods to interact with the Notification
 */
export class NotificationService {

  /**
   * Get unread notification amount
   * @deprecated use NotificationService.getOwnNotificationList().size
   */
  public static async updateEnabledNotification(
    enabledNotificationId: string,
    isEnabled: boolean,
  ): Promise<MwNotificationBffInternalSchemasEnabledNotificationResponse> {
    const enabledNotification = enabledNotificationService.updateEnabledNotification({
      enabledNotificationId,
      request: {isEnabled},
    });

    return enabledNotification;
  }

  /**
   * Get notification list by user ID
   */
  public static async getEnabledNotificationList():
    Promise<MwNotificationBffInternalSchemasGetEnabledNotificationListResponse> {
    const enabledNotificationList = await enabledNotificationService.getEnabledNotificationList();

    return enabledNotificationList;
  }

}
