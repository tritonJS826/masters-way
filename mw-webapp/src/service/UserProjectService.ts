import {
  CreateUserProjectRequest,
  DeleteUserProjectRequest,
} from "src/apiAutogenerated/general";
import {userProjectService} from "src/service/services";

/**
 * Provides methods to interact with the UserProject
 */
export class UserProjectService {

  /**
   * Add user to the project
   */
  public static async addUserProject(requestParameters: CreateUserProjectRequest): Promise<void> {
    await userProjectService.createUserProject(requestParameters);
  }

  /**
   * Delete user from the project
   */
  public static async deleteUserProject(requestParameters: DeleteUserProjectRequest): Promise<void> {
    await userProjectService.deleteUserProject(requestParameters);
  }

}

