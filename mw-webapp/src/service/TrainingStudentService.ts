import {
  CreateTrainingStudentRequest,
  DeleteTrainingStudentRequest,
} from "src/apiAutogenerated/training";
import {trainingStudentService} from "src/service/services";

/**
 * Provides methods to interact with the TrainingStudents
 */
export class TrainingStudentService {

  /**
   * Add student to the training
   */
  public static async createTrainingStudent(requestParameters: CreateTrainingStudentRequest): Promise<void> {
    await trainingStudentService.createTrainingStudent(requestParameters);
  }

  /**
   * Delete student from the training
   */
  public static async deleteTrainingStudent(requestParameters: DeleteTrainingStudentRequest): Promise<void> {
    await trainingStudentService.deleteTrainingStudent(requestParameters);
  }

}
