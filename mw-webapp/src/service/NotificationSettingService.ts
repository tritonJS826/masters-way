import {MwNotificationBffInternalSchemasGetNotificationSettingListResponse} from "src/apiAutogenerated/notification";
import {MwNotificationBffInternalSchemasNotificationSettingResponse}
  from "src/apiAutogenerated/notification/models/MwNotificationBffInternalSchemasNotificationSettingResponse";
import {enabledNotificationService} from "src/service/services";

/**
 * Provides methods to interact with the Notification
 */
export class NotificationService {

  /**
   * Update notification settings
   * @deprecated use NotificationService.getOwnNotificationList().size
   */
  public static async updateEnabledNotification(
    notificationSettingId: string,
    isEnabled: boolean,
  ): Promise<MwNotificationBffInternalSchemasNotificationSettingResponse> {
    const notificationSetting = await enabledNotificationService.updateNotificationSetting({
      notificationSettingId,
      request: {isEnabled},
    });

    return notificationSetting;
  }

  /**
   * Get notification list by user ID
   */
  public static async getEnabledNotificationList():
    Promise<MwNotificationBffInternalSchemasGetNotificationSettingListResponse> {
    const notificationSettingList = await enabledNotificationService.getNotificationSettingList();

    return notificationSettingList;
  }

}
