import {
  CreateTheoryMaterialRequest,
  DeleteTheoryMaterialRequest,
  GetTheoryMaterialsByTopicIdRequest,
  MwTrainingBffInternalSchemasTheoryMaterial,
  MwTrainingBffInternalSchemasTheoryMaterials,
  UpdateTheoryMaterialRequest,
} from "src/apiAutogenerated/training";
import {theoryMaterialService} from "src/service/services";

/**
 * Provides methods to interact with the TheoryMaterials
 */
export class TheoryMaterialService {

  /**
   * Create theory material
   */
  public static async createTheoryMaterial(
    requestParameters: CreateTheoryMaterialRequest,
  ): Promise<MwTrainingBffInternalSchemasTheoryMaterial> {
    const theoryMaterial = await theoryMaterialService.createTheoryMaterial(requestParameters);

    return theoryMaterial;
  }

  /**
   * Get theory materials by topic UUID
   */
  public static async getTheoryMaterialsByUuid(
    requestParameters: GetTheoryMaterialsByTopicIdRequest,
  ): Promise<MwTrainingBffInternalSchemasTheoryMaterials> {
    const theoryMaterials = await theoryMaterialService.getTheoryMaterialsByTopicId(requestParameters);

    return theoryMaterials;
  }

  /**
   * Update theory material
   */
  public static async updateTheoryMaterial(
    requestParameters: UpdateTheoryMaterialRequest,
  ): Promise<MwTrainingBffInternalSchemasTheoryMaterial> {
    const updatedTheoryMaterial = await theoryMaterialService.updateTheoryMaterial(requestParameters);

    return updatedTheoryMaterial;
  }

  /**
   * Delete theory material
   */
  public static async deleteTheoryMaterial(requestParameters: DeleteTheoryMaterialRequest): Promise<void> {
    await theoryMaterialService.deleteTheoryMaterial(requestParameters);
  }

}

