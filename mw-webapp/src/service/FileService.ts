import {MwChatBffInternalSchemasUploadFileResponse, UploadFileRequest} from "src/apiAutogenerated/chat";
import {fileService} from "src/service/services";

/**
 * Provides methods to interact with the files in message
 */
export class FileService {

  /**
   * Upload file
   */
  public static async uploadFile(requestParameters: UploadFileRequest): Promise<MwChatBffInternalSchemasUploadFileResponse> {
    const uploadFile = await fileService.uploadFile(requestParameters);

    return uploadFile;
  }

}
