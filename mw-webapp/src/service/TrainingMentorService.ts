import {
  CreateTrainingMentorRequest,
  DeleteTrainingMentorRequest,
} from "src/apiAutogenerated/training";
import {trainingMentorService} from "src/service/services";

/**
 * Provides methods to interact with the TrainingMentors
 */
export class TrainingMentorService {

  /**
   * Add mentor to the training
   */
  public static async createTrainingMentor(requestParameters: CreateTrainingMentorRequest): Promise<void> {
    await trainingMentorService.createTrainingMentor(requestParameters);
  }

  /**
   * Delete mentor from the training
   */
  public static async deleteTrainingMentor(requestParameters: DeleteTrainingMentorRequest): Promise<void> {
    await trainingMentorService.deleteTrainingMentor(requestParameters);
  }

}

