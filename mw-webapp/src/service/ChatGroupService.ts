import {
  AcceptRequestsToGroupRoomRequest,
  AddUserToGroupRequest,
  DeleteRequestsToGroupRoomRequest,
  DeleteUserToGroupRequest,
  GetGroupRoomByIdRequest,
  MakeMessageInGroupRoomRequest,
  MakeRequestsToGroupRoomRequest,
  UpdateGroupRoomsRequest,
} from "src/apiAutogenerated/chat";
import {chatGroup} from "src/service/services";

/**
 * Provides methods to interact with the group chat
 */
export class ChatGroupService {

  /**
   * Create group chat room
   */
  public static async createGroupRoom(requestParameters: RequestInit): Promise<{ [key: string]: any }> {
    const groupRoom = await chatGroup.createGroupRooms(requestParameters);

    return groupRoom;
  }

  /**
   * Accept requests to group chat room
   */
  public static async acceptRequestsToGroupRoom(requestParameters: AcceptRequestsToGroupRoomRequest): Promise<void> {
    await chatGroup.acceptRequestsToGroupRoom(requestParameters);
  }

  /**
   * Add user to group chat room
   */
  public static async addUserToGroup(requestParameters: AddUserToGroupRequest): Promise<void> {
    await chatGroup.addUserToGroup(requestParameters);
  }

  /**
   * Delete requests to group chat room
   */
  public static async deleteRequestsToGroupRoom(requestParameters: DeleteRequestsToGroupRoomRequest): Promise<void> {
    await chatGroup.deleteRequestsToGroupRoom(requestParameters);
  }

  /**
   * Delete user from group chat room
   */
  public static async deleteUserFromGroup(requestParameters: DeleteUserToGroupRequest): Promise<void> {
    await chatGroup.deleteUserToGroup(requestParameters);
  }

  /**
   * Ger group chat room by ID
   */
  public static async getGroupRoomById(requestParameters: GetGroupRoomByIdRequest): Promise<{ [key: string]: any }> {
    const groupRoom = await chatGroup.getGroupRoomById(requestParameters);

    return groupRoom;
  }

  /**
   * Get group chat rooms
   */
  public static async getGroupRooms(requestParameters: RequestInit): Promise<{ [key: string]: any }> {
    const groupRooms = await chatGroup.getGroupRooms(requestParameters);

    return groupRooms;
  }

  /**
   * Get requests to group chat room
   */
  public static async getRequestsToGroupRoom(requestParameters: RequestInit): Promise<{ [key: string]: any }> {
    const requestsToGroupRoom = await chatGroup.getRequestsToGroupRoom(requestParameters);

    return requestsToGroupRoom;
  }

  /**
   * Make message in group chat room
   */
  public static async makeMessageInGroupRoom(requestParameters: MakeMessageInGroupRoomRequest): Promise<{ [key: string]: any }> {
    const message = await chatGroup.makeMessageInGroupRoom(requestParameters);

    return message;
  }

  /**
   * Make requests to group chat room
   */
  public static async makeRequestsToGroupRoom(
    requestParameters: MakeRequestsToGroupRoomRequest,
  ): Promise<void> {
    await chatGroup.makeRequestsToGroupRoom(requestParameters);
  }

  /**
   * Update group chat room
   */
  public static async updateGroupRooms(requestParameters: UpdateGroupRoomsRequest): Promise<{ [key: string]: any }> {
    const groupRoomUpdated = await chatGroup.updateGroupRooms(requestParameters);

    return groupRoomUpdated;
  }

}
