import {
  CreateUserTagRequest,
  DeleteUserTagRequest,
  MwServerInternalSchemasUserTagResponse,
} from "src/apiAutogenerated/general";
import {skillService} from "src/service/services";

/**
 * Provides methods to interact with the skills
 */
export class SkillService {

  /**
   * Create skill
   */
  public static async createSkill(requestParameters: CreateUserTagRequest): Promise<MwServerInternalSchemasUserTagResponse> {
    const skill = await skillService.createUserTag(requestParameters);

    return skill;
  }

  /**
   * Delete skill by UUID
   */
  public static async deleteSkill(requestParameters: DeleteUserTagRequest): Promise<void> {
    await skillService.deleteUserTag(requestParameters);
  }

}
