import {
  CreateProjectRequest,
  DeleteProjectRequest,
  GetProjectRequest,
  MwServerInternalSchemasProjectPopulatedResponse,
  UpdateProjectRequest,
} from "src/apiAutogenerated/general";
import {projectService} from "src/service/services";

/**
 * Provides methods to interact with the Project
 */
export class ProjectService {

  /**
   * Create project
   */
  public static async createProject(
    requestParameters: CreateProjectRequest,
  ): Promise<MwServerInternalSchemasProjectPopulatedResponse> {
    const project = await projectService.createProject(requestParameters);

    return project;
  }

  /**
   * Get project by UUID
   */
  public static async getProjectByUuid(
    requestParameters: GetProjectRequest,
  ): Promise<MwServerInternalSchemasProjectPopulatedResponse> {
    const project = await projectService.getProject(requestParameters);

    return project;
  }

  /**
   * Update project by UUID
   */
  public static async updateProject(
    requestParameters: UpdateProjectRequest,
  ): Promise<MwServerInternalSchemasProjectPopulatedResponse> {
    const project = await projectService.updateProject(requestParameters);

    return project;
  }

  /**
   * Delete project by UUID
   */
  public static async deleteProject(requestParameters: DeleteProjectRequest): Promise<void> {
    await projectService.deleteProject(requestParameters);
  }

}

