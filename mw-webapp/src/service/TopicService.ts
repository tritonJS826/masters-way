import {
  CreateTopicsRequest,
  DeleteTopicRequest,
  GetTopicRequest,
  MwTrainingBffInternalSchemasTopic,
  MwTrainingBffInternalSchemasTopicPreview,
  MwTrainingBffInternalSchemasTopicsPreview,
  UpdateTopicRequest,
} from "src/apiAutogenerated/training";
import {topicService} from "src/service/services";

/**
 * Provides methods to interact with the Topics
 */
export class TopicService {

  /**
   * Create topic
   */
  public static async createTopics(requestParameters: CreateTopicsRequest): Promise<MwTrainingBffInternalSchemasTopicsPreview> {
    const topics = await topicService.createTopics(requestParameters);

    return topics;
  }

  /**
   * Get topic by Uuid
   */
  public static async getTopic(requestParameters: GetTopicRequest): Promise<MwTrainingBffInternalSchemasTopic> {
    const topic = await topicService.getTopic(requestParameters);

    return topic;
  }

  /**
   * Update topic
   */
  public static async updateTopic(requestParameters: UpdateTopicRequest): Promise<MwTrainingBffInternalSchemasTopicPreview> {
    const updatedTopic = await topicService.updateTopic(requestParameters);

    return updatedTopic;
  }

  /**
   * Delete topic
   */
  public static async deleteTopic(requestParameters: DeleteTopicRequest): Promise<void> {
    await topicService.deleteTopic(requestParameters);
  }

}

