import {
  CreateTopicRequest,
  DeleteTopicRequest,
  MwTrainingBffInternalSchemasTopic,
  UpdateTopicRequest,
} from "src/apiAutogenerated/training";
import {topicService} from "src/service/services";

/**
 * Provides methods to interact with the Topics
 */
export class TopicService {

  /**
   * Create topic
   */
  public static async createTopic(requestParameters: CreateTopicRequest): Promise<MwTrainingBffInternalSchemasTopic> {
    const topic = await topicService.createTopic(requestParameters);

    return topic;
  }

  /**
   * Update topic
   */
  public static async updateTopic(requestParameters: UpdateTopicRequest): Promise<MwTrainingBffInternalSchemasTopic> {
    const updatedTopic = await topicService.updateTopic(requestParameters);

    return updatedTopic;
  }

  /**
   * Delete topic
   */
  public static async deleteTopic(requestParameters: DeleteTopicRequest): Promise<void> {
    await topicService.deleteTopic(requestParameters);
  }

}

