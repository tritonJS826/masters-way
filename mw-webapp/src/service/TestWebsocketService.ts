import {
  HostStartedGameRequest,
  MwTestWebsocketInternalSchemasUserJoinedSessionEventResponse,
  UserCapturedTargetRequest,
  UserJoinedSessionRequest,
  UserReadyToStartPlayRequest,
} from "src/apiAutogenerated/testWebsocket";
import {testWebsocketService} from "src/service/services";

/**
 * Provides methods to interact with the test-websocket
 */
export class TestWebsocketService {

  /**
   * Send user joined session event
   */
  public static async sendUserJoinedSessionEvent(
    requestParameters: UserJoinedSessionRequest,
  ): Promise<MwTestWebsocketInternalSchemasUserJoinedSessionEventResponse> {
    const data = await testWebsocketService.userJoinedSession(requestParameters);

    return data;
  }

  /**
   * Send User ready to start play event
   */
  public static async sendUserReadyToStartPlayEvent(
    requestParameters: UserReadyToStartPlayRequest,
  ): Promise<void> {
    const room = await testWebsocketService.userReadyToStartPlay(requestParameters);

    return room;
  }

  /**
   * Send host started game event
   */
  public static async sendHostStartedGameEvent(
    requestParameters: HostStartedGameRequest,
  ): Promise<void> {
    const data = await testWebsocketService.hostStartedGame(requestParameters);

    return data;
  }

  /**
   * Send user captured target event
   */
  public static async sendUserCapturedTargetEvent(requestParameters: UserCapturedTargetRequest): Promise<void> {
    const data = await testWebsocketService.userCapturedTarget(requestParameters);

    return data;
  }

}
