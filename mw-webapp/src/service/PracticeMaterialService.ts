import {
  CreatePracticeMaterialRequest,
  DeletePracticeMaterialRequest,
  GetPracticeMaterialsByTopicIdRequest,
  MwTrainingBffInternalSchemasPracticeMaterial,
  MwTrainingBffInternalSchemasPracticeMaterials,
  UpdatePracticeMaterialRequest,
} from "src/apiAutogenerated/training";
import {practiceMaterialService} from "src/service/services";

/**
 * Provides methods to interact with the PracticeMaterials
 */
export class PracticeMaterialService {

  /**
   * Create practice material
   */
  public static async createPracticeMaterial(
    requestParameters: CreatePracticeMaterialRequest,
  ): Promise<MwTrainingBffInternalSchemasPracticeMaterial> {
    const practiceMaterial = await practiceMaterialService.createPracticeMaterial(requestParameters);

    return practiceMaterial;
  }

  /**
   * Get practice materials by topic UUID
   */
  public static async getPracticeMaterialsByTopicUuid(
    requestParameters: GetPracticeMaterialsByTopicIdRequest,
  ): Promise<MwTrainingBffInternalSchemasPracticeMaterials> {
    const practiceMaterials = await practiceMaterialService.getPracticeMaterialsByTopicId(requestParameters);

    return practiceMaterials;
  }

  /**
   * Update practice material
   */
  public static async updatePracticeMaterial(
    requestParameters: UpdatePracticeMaterialRequest,
  ): Promise<MwTrainingBffInternalSchemasPracticeMaterial> {
    const updatedPracticeMaterial = await practiceMaterialService.updatePracticeMaterial(requestParameters);

    return updatedPracticeMaterial;
  }

  /**
   * Delete practice material
   */
  public static async deletePracticeMaterial(requestParameters: DeletePracticeMaterialRequest): Promise<void> {
    await practiceMaterialService.deletePracticeMaterial(requestParameters);
  }

}

