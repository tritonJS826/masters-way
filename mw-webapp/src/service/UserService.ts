import {
  CreateUserRequest,
  DeleteUserRequest,
  GetUserByUuidRequest,
  SchemasGetAllUsersResponse,
  SchemasUserPlainResponse,
  SchemasUserPopulatedResponse,
  UpdateUserRequest,
} from "src/apiAutogenerated";
import {userService} from "src/service/services";

/**
 * Pagination and filter params
 */
export interface GetUsersParams {

  /**
   * Last fetched way uuid
   */
  lastUserUuid?: string;

  /**
   * FilterEmail
   */
  email?: string;
}

/**
 * Provides methods to interact with the Users
 */
export class UserServiceU {

  /**
   * Get all users
   */
  public static async getAllUsers(): Promise<SchemasGetAllUsersResponse> {
    const allUsers = await userService.getAllUsers();

    return allUsers;
  }

  /**
   * Create user
   */
  public static async createUser(requestParameters: CreateUserRequest): Promise<SchemasUserPlainResponse> {
    const user = await userService.createUserIfRequired(requestParameters);

    return user;
  }

  /**
   * Get user by UUID
   */
  public static async getUserByUuid(requestParameters: GetUserByUuidRequest): Promise<SchemasUserPopulatedResponse> {
    const user = await userService.getUserByUuid(requestParameters);

    return user;
  }

  /**
   * Update user
   */
  public static async updateUser(requestParameters: UpdateUserRequest): Promise<SchemasUserPlainResponse> {
    const updatedUser = await userService.updateUser(requestParameters);

    return updatedUser;
  }

  /**
   * Delete user
   */
  public static async deleteUser(requestParameters: DeleteUserRequest): Promise<void> {
    await userService.deleteUser(requestParameters);
  }

}
