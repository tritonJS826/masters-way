import {
  CreateTrainingRequest,
  DeleteTrainingRequest,
  GetTrainingByIdRequest,
  GetTrainingListByUserRequest,
  GetTrainingListRequest,
  MwTrainingBffInternalSchemasTraining,
  MwTrainingBffInternalSchemasTrainingList,
  UpdateTrainingRequest,
} from "src/apiAutogenerated/training";
import {trainingService} from "src/service/services";

/**
 * Pagination and filter params
 */
export interface GetTrainingParams {

  /**
   * Page
   */
  page?: number;

  /**
   * Limit
   */
  limit?: number;

  /**
   * Part of training name
   */
  trainingName: string;

}

/**
 * Provides methods to interact with the Trainings
 */
export class TrainingService {

  /**
   * Get all trainings
   */
  public static async getAllTrainings(
    requestParameters: GetTrainingListRequest = {},
  ): Promise<MwTrainingBffInternalSchemasTrainingList> {
    const allTrainings = await trainingService.getTrainingList(requestParameters);

    return allTrainings;
  }

  /**
   * Get trainings by user Uuid
   */
  public static async getTrainingsByUserUuid(
    requestParameters: GetTrainingListByUserRequest,
  ): Promise<MwTrainingBffInternalSchemasTrainingList> {
    const allTrainings = await trainingService.getTrainingListByUser(requestParameters);

    return allTrainings;
  }

  /**
   * Create training
   */
  public static async createTraining(requestParameters: CreateTrainingRequest): Promise<MwTrainingBffInternalSchemasTraining> {
    const training = await trainingService.createTraining(requestParameters);

    return training;
  }

  /**
   * Get training by UUID
   */
  public static async getTrainingByUuid(
    requestParameters: GetTrainingByIdRequest,
  ): Promise<MwTrainingBffInternalSchemasTraining> {
    const training = await trainingService.getTrainingById(requestParameters);

    return training;
  }

  /**
   * Update training
   */
  public static async updateTraining(requestParameters: UpdateTrainingRequest): Promise<MwTrainingBffInternalSchemasTraining> {
    const updatedTraining = await trainingService.updateTraining(requestParameters);

    return updatedTraining;
  }

  /**
   * Delete training
   */
  public static async deleteTraining(requestParameters: DeleteTrainingRequest): Promise<void> {
    await trainingService.deleteTraining(requestParameters);
  }

}

