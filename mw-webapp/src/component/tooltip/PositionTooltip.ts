/**
 * Tooltip position relative to child
 * @public
 */
export const enum PositionTooltip {

  /**
   * Position Tooltip is above the cursor relative to the element
   */
  TOP = "top",

  /**
   * Position Tooltip to the left of the cursor relative to the element
   */
  LEFT = "left",

  /**
   * Position Tooltip to the right of the cursor relative to the element
   */
  RIGHT = "right",

  /**
   * Position Tooltip is below the cursor relative to the element
   */
  BOTTOM = "bottom",

  /**
   * Position Tooltip is below and left the cursor relative to the element
   */
  BOTTOM_LEFT = "bottomLeft",

  /**
   * Position Tooltip is below and right the cursor relative to the element
   */
  BOTTOM_RIGHT = "bottomRight",

  /**
   * Position Tooltip is above and left the cursor relative to the element
   */
  TOP_LEFT = "topLeft",

  /**
   * Position Tooltip is above and right the cursor relative to the element
   */
  TOP_RIGHT = "topRight",
}
