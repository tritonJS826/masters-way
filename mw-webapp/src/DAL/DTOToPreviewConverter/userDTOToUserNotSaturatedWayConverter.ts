import {SchemasUserPlainResponseWithInfo} from "src/apiAutogenerated";
import {UserNotSaturatedWay} from "src/model/businessModelPreview/UserNotSaturatedWay";
import {USER_CREATED_AT_FIELD} from "src/model/DTOModel/UserDTO";

/**
 * Convert {@link UserDTO} to {@link UserPreview}
 */
export const UserDTOToUserNotSaturatedWayConverter = (userDTO: SchemasUserPlainResponseWithInfo): UserNotSaturatedWay => {
  return new UserNotSaturatedWay({
    ...userDTO,
    favoriteWays: 0,
    createdAt: new Date(userDTO[USER_CREATED_AT_FIELD]),
    favoriteForUserUuids: [],
    favoriteUserUuids: [],
    imageUrl: userDTO.imageUrl ?? "",
  });
};
