import {SchemasUserPopulatedResponse} from "src/apiAutogenerated";
import {User} from "src/model/businessModel/User";
import {USER_CREATED_AT_FIELD} from "src/model/DTOModel/UserDTO";

/**
 * Convert {@link UserDTO} to {@link User}
 */
export const UserDTOToUserConverter = (userDTO: SchemasUserPopulatedResponse): User => {
  return new User({
    ...userDTO,
    ownWays: [],
    favoriteWays: [],
    mentoringWays: [],
    customWayCollections: [],
    createdAt: new Date(userDTO[USER_CREATED_AT_FIELD]),
    favoriteForUserUuids: [],
    favoriteUserUuids: [],
    imageUrl: userDTO.imageUrl ?? "",
    wayRequests: [],
  });
};
