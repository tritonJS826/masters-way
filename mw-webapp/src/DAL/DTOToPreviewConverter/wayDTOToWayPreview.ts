import {SchemasMetricResponse, SchemasWayPlainResponse} from "src/apiAutogenerated";
import {WayPreview} from "src/model/businessModelPreview/WayPreview";

/**
 * Convert {@link SchemasWayPlainResponse} to {@link WayPreview}
 */
export const wayDTOToWayPreview = (wayDTO: SchemasWayPlainResponse, metrics: SchemasMetricResponse[]): WayPreview => {
  return new WayPreview({
    ...wayDTO,
    metrics,
    createdAt: new Date(wayDTO.createdAt),
    lastUpdate: new Date(wayDTO.updatedAt),
  });
};
